            <h5>Some remembrances from Peggy (Steele) Blackford about her mother, Margaret Vanja (Bengtsson) Steele … </h5>

            <i>
                <p class="tab">
                    My mother was the oldest of 9 children and had a lot of responsibilities. When she was young and in America the house she lived in was very full and usually had about 15 people staying there. With so many people and not much money life had to be hard. Men worked and women worked and did house things. Granma and Grandpa Bengtsson had a three bedroom house and people slept on porches when the weather allowed. Margit Vanja (my mother) took her senior year off from high-school and worked. Because she skipped a year of school mother and her sister Francis graduated high-school together. Mother (Margit Vanja) worked at JC Penny’s around that time. There is a photo of her with the staff and Mr. Penny. She was very proud of that photo.</p>

                <p class="tab">
                    Mother also had an older half-brother, Nils Carlson. Nils was the son of grandpa Carl Bengtsson and a woman back in Sweden. This was before the family left for America. The topic of Nils was not taboo around the house but not clearly and openly discussed. Grandpa and Grandma Bengtsson would always say cousin Nils was coming to visit. Yes, Granma Agnes always knew about Nils. Not sure when mother (Margit Vanja) found out she had a half-brother but I believe before mother and daddy (George Steele) were married. I remember uncle Nils visiting us when we lived in Atlanta. There is a picture of Betty (Nils mom) somewhere. Mother visited her in Sweden.</p>

                <p class="tab">
                    Nils was living at Stormon when he had his 1st stroke. Mother, his half-sister, couldn't get ahold of him on the phone and had someone drive her there to check on him. If she hadn't gone he would never have gotten help. Margit Vanja always visited him when she was there in Sweden.</p>

                <p class="tab">
                    There are pictures of Margit Vanja with Nils, and Hedvic having coffee at Hedvic's apartment. Even some pictures of uncle Phil, Aunt Judy, Nils and Margit Vanja there at Hedvic's.</p>

                <p class="tab">
                    Mother (Margit Vanja) kept many of the Swedish traditions when we were little and throughout her life. Christmas was always full of Swedish things from food to decorations. However, I don't remember if we had ham or turkey. When we lived close to Grandma and grandpa Bengtsson were always at their home for the holidays. I remember real candles on the Christmas tree. And the feast!</p>

                <p class="tab">
                    Daddy had his first heart attack when he was 44 (around 1952). He died at 56 (1965) and Drs were amazed he was able to have 12 more years. </p>

                <p class="tab">
                    Mother (Margit Vanja) was devastated when her husband died. She knew he had heart problems but never accepted them. I remember daddy (George Steele) putting important information on the dining room table to show and explain to her but she said either later or not now. Her friend Hilder Guitler called Carol, Ginger and I to say she needed help. Aunt Ginger was the one that realized someone needed to do something and she did. So blessed she did. Ginger helped mother move to a small apartment in Knoxville, TN, close to Ginger’s house. </p>
            </i>

